# String Slicing
name="Vinay,Shah"
print(len(name))
# print(name[0:5]) including 0 to 4 5 not include
print(name[:5])
print(name[1:5])

# Total 10 character in name but 0 to -3 means total - 3 which is total 7 character
print(name[0:-3])  

# index 10-3=7,10-1=9 it means 7 to 9 character print 9 exclude
print(name[-3:-1])
